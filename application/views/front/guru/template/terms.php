<p class="bold center">Syarat dan Ketentuan</p>
<p class="bold center">Menjadi Guru di Ruangguru</p>
<br/>
<p>Ruangguru adalah sebuah website yang menghubungkan murid dengan guru yang tepat untuk menambah pelajaran di luar sekolah, atau mengembangkan keahlian tertentu. Website www.ruangguru.com ini dikelola oleh PT. Ruang Raya Indonesia.</p>
<br/>
<p>Dengan mengakses dan menggunakan website www.ruangguru.com serta mendaftar menjadi Guru di www.ruangguru.com, berarti Anda telah memahami dan menyetujui untuk terikat dan tunduk dengan semua peraturan yang berlaku di Ruangguru.</p>
<br/>
<ol class="number-list">
<li><p class="bold">Pendaftaran</p>
	<ol>
		<li>Untuk registrasi menjadi Guru, Anda harus mengisi semua kelengkapan biodata tanpa terkecuali dan menyertakan identitas asli Anda.</li>
		<li>Anda harus mencantumkan nama lengkap (bukan nama alias).</li>
		<li>Nomor telepon yang dicantumkan adalah nomor yang aktif, sehingga Ruangguru dapat menghubungi sewaktu-waktu apabila diperlukan.</li>
		<li>Ruangguru berhak untuk menolak aplikasi pendaftaran Anda sebagai Guru dengan alasan apapun.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Akun Guru</p>
	<ol>
		<li>Anda bertanggung jawab untuk menjaga kerahasiaan akun dan password dan untuk membatasi akses ke komputer Anda.</li>
		<li>Anda setuju bertanggung jawab atas semua yang terjadi di bawah akun Anda dengan password Anda.</li>
		<li>Anda setuju untuk segera memberitahukan Ruangguru tentang setiap penyalahgunaan akun atau pelanggaran keamanan lainnya yang berkaitan dengan akun Anda.</li>
		<li>Ruangguru tidak akan bertanggung jawab atas kerugian atau kerusakan yang timbul dari kegagalan untuk mematuhi persyaratan-persyaratan ini.</li>
		<li>Ruangguru berhak untuk menolak layanan atau menghentikan akun.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Profil Guru</p>
	<ol>
		<li>Anda setuju untuk mengisi semua informasi pribadi seakurat mungkin dan tidak akan berusaha mengelabui pengguna layanan Ruangguru.</li>
		<li>Anda, dan bukan Ruangguru, bertanggung jawab penuh atas semua materi yang Anda sediakan melalui Ruangguru.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Kualifikasi dan Sertifikat</p>
	<ol>
		<li>Anda setuju untuk mengisi kualifikasi dan data lain seakurat dan selengkap mungkin.</li>
		<li>Anda setuju untuk dapat menunjukkan sertifikat kualifikasi asli Anda kepada orang tua atau pengguna layanan Ruangguru jika diminta.</li>
		<li>Ruangguru berhak untuk meminta salinan sertifikat asli Anda untuk verifikasi.</li>
		<li>Hanya sertifikat terverifikasi yang akan diberikan poin Rating.</li>
		<li>Ruangguru berhak untuk mengubah skema Rating untuk lebih memenuhi kebutuhan pelanggan Ruangguru.</li>
		<li>Gambar sertifikat tidak tersedia untuk umum. Ruangguru dapat memperlihatkan sertifikat dalam format PDF ke pelanggan setelah Anda telah ditugaskan untuk pelajaran pertama.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Permintaan baru</p>
	<ol>
		<li>Ruangguru dapat membuat daftar permintaan belajar untuk Anda secara online.</li>
		<li>Anda dapat menunjukkan minat Anda dalam permintaan belajar ini tetapi penugasan akan disesuaikan dengan kebutuhan dan preferensi pelanggan.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Penerimaan SMS</p>
	<ol>
		<li>Anda setuju bahwa Ruangguru dapat mengirimkan SMS, email dan telepon yang berhubungan dengan permintaan mengajar atau pengingat pelajaran pertama.</li>
		<li>Jika Anda tidak menanggapi <i>broadcast</i> SMS, tidak membalas email dan tidak menjawab telepon, maka status akun Anda akan ditetapkan ke 'Sibuk' secara otomatis.</li>
		<li>Ruangguru tidak menjamin bahwa Anda akan menerima <i>broadcast</i> SMS, email dan telepon. Ini akan bergantung pada profil Anda, rating Anda, dan kebutuhan atau preferensi pelanggan.</li>
		<li>Ruangguru tidak menjamin bahwa Anda akan menerima penugasan mengajar setelah Anda menerima dan menanggapi <i>broadcast</i> SMS, email dan telepon.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Pelajaran pertama dan Penugasan</p>
	<ol>
		<li>Anda setuju untuk menghubungi pelanggan sebelum pelajaran pertama untuk konfirmasi informasi kegiatan belajar mengajar.</li>
		<li>Jika Anda gagal muncul untuk pelajaran pertama atau ada alasan pembatalan lain setelah Anda membuat konfirmasi tugas mengajar, Ruangguru akan menutup akun Guru Anda. Ini akan dievaluasi berdasarkan kasus per kasus.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Pembatalan penugasan
	<ol>
		<li>Tidak ada kontrak antara Anda dan pelanggan dan tidak ada komitmen untuk menyelesaikan jumlah pelajaran yang ditetapkan dari sisi pelanggan.</li>
		<li>Pelanggan dapat berhenti setiap saat.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Komisi</p>
	<ol>
		<li>Ruangguru akan mendapatkan 20% dari penghasilan total sebagai komisi.</li>
		<li>Ruangguru tidak memungkinkan komisi yang akan dikumpulkan dalam angsuran.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Pembayaran</p>
	<ol>
		<li>Pembayaran guru akan diberikan 50% pada pertemuan belajar pertama dengan murid dan sisanya sebesar 50% dibayar di akhir setiap paket belajar.</li>
		<li>Apabila Anda tidak menerima pembayaran dan bukti pembayaran dalam waktu 2x24 jam maka segera hubungi pihak Ruangguru.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Perilaku Guru</p>
	<ol>
		<li>Guru harus menjaga etika dan kesopanan saat belajar dengan Murid agar suasana belajar mengajar menjadi kondusif dan nyaman.</li>
		<li><b>Ketepatan waktu</b>. Guruyang terlambat untuk pelajaran pertama akan berdampak pada penutupan akun Guru. Jika ada keluhan terlambat untuk pelajaran selanjutnya, kami akan memberikan penalti pada rating guru.</li>
		<li><b>Tanggung Jawab</b>. Guru yang berhenti tanpa memberitahu pelanggan melalui telepon atau secara langsung akan berdampak pada penutupan akun Guru. SMS atau email tidak dianggap sebagai sarana untuk menginformasikan pelanggan.</li>
		<li><b>Profesionalisme</b>. Guru harus berkonsentrasi pada pelajaran. Kami akan memberikan penalti rating guru sesuai dengan tingkat keseriusan keluhan pelanggan.</li>
		<li><b>Cara berpakaian</b>. Guru harus berpakaian yang rapi untuk pelajaran.</li>
		<li>Semua keluhan terhadap Guru akan dievaluasi berdasarkan kasus per kasus dan Guru akan diberikan kesempatan untuk membantah keluhan.</li>
	</ol>
</li>
<br/>
<li><p class="bold">Penggunaan Informasi</p>
<p>Penggunaan informasi akun guru, duta dan murid untuk kepentingan Ruangguru maupun Indonesian Future Leaders diperbolehkan dengan syarat tetap oleh persetujuan pihak Ruangguru tanpa melalui perantara dan segala hal yang berkaitan informasi adalah hak Ruangguru sepenuhnya.</li>
</li>
</ol>
