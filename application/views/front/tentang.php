						 <h3><p class="bold text-14 orange">Mekanisme Request Kelas</p></h3>
						 <p>Request Kelas pada prinsipnya merupakan sebuah layanan yang ditulis oleh murid yang sedang mencari guru. Murid menuliskan kriteria guru yang ingin dicari, waktu belajar yang diinginkan, lokasi belajar, serta range biaya yang tersedia. Selanjutnya tim Ruangguru akan mencarikan guru yang sesuai, dan memberikan sejumlah rekomendasi kepada murid. Jika pilihan guru telah disepakati, selanjutnya murid dapat melakukan transaksi pembayaran layaknya proses pemesanan guru secara regular melalui Ruangguru.</p>
						 <h3><p class="bold text-14 orange">Cara Murid Membayar Guru</p></h3>
						 <p>Setelah memilih guru yang diinginkan berdasarkan pilihan yang ada, Ruangguru akan melakukan konfirmasi kepada guru yang dipilih. Jika guru pilihan pertama tidak bersedia, maka pihak Ruang Guru akan meneruskan ke pilihan kedua, dan seterusnya hingga pilihan ketiga. Jika ketiga pilihan yang ditetapkan tidak ada yang bersedia, maka Ruangguru akan mencarikan opsi lain dan menawarkannya kepada murid.</p>
						 <p>Jika murid telah menyepakati guru yang dipilih, selanjutnya pihak Ruangguru akan mengirimkan invoice (tagihan pembayaran), dan kemudian murid dapat melakukan pembayaran 100% dari biaya paket belajar yang ditetapkan melalui rekening Ruangguru</p>
						 <p>Setelah melakukan pembayaran, murid perlu melakukan konfirmasi pembayaran melalui salah satu opsi di bawah ini:</p>
