							<li>Murid akan menerima konfirmasi penerimaan biaya yang ditransfer, dan kemudian dapat memulai kegiatan belajar sesuai yang sudah dijadwalkan.</li>
							<li>Kegiatan belajar tidak akan dimulai jika murid belum melakukan pembayaran biaya paket belajar yang dipilih.</li>
