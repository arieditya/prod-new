						  <h3><p class="bold text-14 orange">Kriteria Guru di Ruangguru</p></h3>
						  <p>Untuk menjadi Guru di Ruangguru, Anda tidak harus memiliki pendidikan formal di bidang keguruan atau memiliki pengalaman bertahun-tahun mengajar, tapi Anda yang masih baru pun juga bisa! Meskipun begitu, calon murid akan memilih sendiri calon gurunya, sehingga bagaimana Anda sebagai Calon Guru menampilkan diri dalam profil di website akan sangat menunjang potensi Anda untuk dipilih</p>
						  <h3><p class="bold text-14 orange">Pelajaran yang diajar oleh Guru</p></h3>
						  <p>Pada prinsipnya Ruangguru bukan hanya fokus untuk memfasilitasi kegiatan belajar tambahan untuk pelajaran-pelajaran yang diajarkan di sekolah saja (TK, SD, SMP, SMA, dan Universitas), namun juga pelajaran untuk persiapan UN, SNMPTN, ataupun tes standardisasi lainnya (seperti TOEFL, IELTS, GRE, GMAT, dll). Selain itu Anda juga dapat mengajarkan bahasa asing, maupun jenis keterampilan lainnya.</p>
						  <p>Jika bidang studi yang Anda ajarkan belum tertera di Ruangguru.com Anda dapat mengajukannya melalui email ke <a href="mailto:info@ruangguru.com" class="normal-link bold">info@ruangguru.com</a>.</p>
						  <h3><p class="bold text-14 orange">Lokasi mengajar Guru</p></h3>
						  <p>Pada umumnya guru mengajar di rumah siswa, atau tempat lainnya yang telah disepakati dengan siswa.</p>
						  <h3><p class="bold text-14 orange">Mekanisme mengajar di Ruangguru</p></h3>
						  <p>Hingga saat ini Ruangguru belum menyediakan mekanisme pengajaran secara online. Meskipun begitu, jika guru secara personal ingin menginisiasikannya guna memfasilitas siswa yang belajar jarak jauh, dan siswa juga berkenan, pihak Ruangguru mempersilahkan hal tersebut.</p>
						  <h3><p class="bold text-14 orange">Pembayaran Guru di Ruangguru</p></h3>
						 <p>Ya, setiap guru yang mengajar melalui Ruangguru dibayar berdasarkan paket belajar yang ditetapkannya sendiri. Singkat kata, Anda yang menetapkan berapa Anda ingin dibayar. Ruangguru menggunakan sistem pembayaran per jam, dimana setiap guru minimal menetapkan harga minimal Rp 50,000,-/ jam, dan maksimal tak terbatas. Biaya yang dialokasikan ini sudah termasuk biaya transportasi, ataupun pengeluaran lainnya yang dikeluarkan oleh guru, dan potongan sebesar 20% dari biaya/ jam untuk pihak Ruangguru.</p>
						 <p>Setiap guru disarankan untuk membuat paket belajar sendiri. Contohnya jika guru menetapkan paket 8x pertemuan (2 jam/ pertemuan), dengan biaya Rp 100,000,- per jam, maka setiap guru akan memperoleh Rp 160,000,- / pertemuan (sudah dipotong 20% untuk biaya operasional Ruangguru dan kontribusi sosial). Untuk itu, dalam menentukan harga, ada baiknya Anda mempertimbangkan pengeluaran serta potongan yang diberlakukan oleh pihak Ruangguru.</p>
						 <p>Setelah murid menyepakati guru pilihannya, pihak Ruangguru akan mengirimkan tagihan kepada murid untuk dibayarkan kepada Ruangguru. Guru akan dibayar sesuai dengan biaya yang disepakati sebesar 50% setelah melakukan pertemuan belajar 1 dengan siswa, dan sisa 50% lagi akan dibayar di akhir setiap paket belajar.</p>
						 <h3><p class="bold text-14 orange">Penilaian performa Guru di Ruangguru</p></h3>
						 <p>Ruangguru memiliki sistem rating yang komprehensif yang melibatkan murid untuk menilai performa guru dalam mengajar, dan juga memberikan poin terhadap latar belakang guru. Ruangguru memberikan poin lebih kepada guru yang memiliki prestasi akademis yang baik dan juga pengalaman mengajar yang mumpuni. Guru dapat meunggah bukti prestasi akademis/ sertifikasi/ pengalamannya, dimana pihak Ruangguru akan memverifikasi data-data tersebut. Guru juga didorong untuk menuliskan pesan dan profilnya dengan lengkap dan semenarik mungkin, serta memperbaharui ketersediaan jadwal mengajarnya secara berkala dan detil. </p>
