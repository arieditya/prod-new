                    Apakah Guru dibayar?
                </div>
                <div class="bfcs-faq-answer justify">
                    Ya, setiap guru yang mengajar melalui Ruangguru dibayar berdasarkan paket belajar yang ditetapkan masing-masing guru. Setiap guru berhak menetapkan harga minimal Rp 50,000,-/ jam, dan nominal maksimal yang tak terbatas. Perlu diketahui bahwa biaya yang ditetapkan sebagai harga paket belajar ini sudah termasuk biaya transportasi, ataupun pengeluaran lainnya yang dikeluarkan oleh guru, dan potongan sebesar 20% dari biaya/ jam untuk pihak Ruangguru. 
				<br/>
				<br/>
