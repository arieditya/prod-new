    <div class="bfcs-list">
        <ul>
            <li>
                <div class="bfcs-faq-duta-question bold">Apa itu Duta Ruangguru?</div>
                <div class="bfcs-faq-answer justify">
                    Duta Ruangguru merupakan pihak ketiga (bisa berupa individu/ komunitas) yang membantu pemasaran dan promosi Ruangguru, utamanya dalam merekrut calon guru dan calon murid. Dalam berkegiatan, Duta Ruangguru akan dilengkapi dengan materi promosi, mendapatkan komisisetiap berhasil mendapatkan referral (sebesar 3% dari nilai paket belajar). <span class="red-notif">*</span>
                    <br/><br/>
                    <span class="text-13 red-notif">*</span><span class="text-11"> Mekanisme referral fee akan diatur lebih lanjut, dan yang dihitung sebagai kompensasi adalah jika calon murid mendaftar minimal 1 paket belajar dan (atau) guru dipekerjakan.</span>
                </div>
            </li>
            <li>
                <div class="bfcs-faq-duta-question bold">Apa saja manfaat yang diperoleh oleh Duta Ruangguru?</div>
                <div class="bfcs-faq-answer justify">
                    Selain berkesempatan untuk menambah penghasilan, menjadi Duta Ruangguru juga merupakan pengalaman berharga yang akan mendukung <i>soft skills</i> Anda. Setidaknya <i>skills set</i> yang tercakup dalam aktivitas ini adalah manajemen proyek, komunikasi, networking, marketing, dan lainnya. 
				<br/><br/>
				Duta Ruangguru yang aktif dan memenuhi target, akan memperoleh sertifikat dan kompensasi dari Pihak Ruangguru.
			 </div>
            </li>
            <li>
                <div class="bfcs-faq-duta-question bold">Bagaimana mekanisme pembayaran Duta Ruangguru?</div>
                <div class="bfcs-faq-answer justify">
                    Duta Ruangguru akan menerima bonus setiap berhasil mendapatkan murid yang mendaftar minimal 1 paket belajar di Ruangguru, atau jika guru yang didaftarkan berhasil dipilih oleh calon murid. Referral murid akan dibayar setelah murid tersebut melakukan paket belajar. Referral guru akan dibayar setelah guru tersebut melakukan paket belajar pertama (hanya untuk paket belajar pertama dan tidak berlaku untuk paket belajar selanjutnya). Transaksi pembayaran dapat diakses secara reguler melalui akun profil Duta Ruang Guru di website Ruangguru.com
                </div>
            </li>
            <li>
                <div class="bfcs-faq-duta-question bold">Bagaimana cara menjadi Duta Ruangguru?</div>
                <div class="bfcs-faq-answer justify">
                    Untuk menjadi duta di Ruangguru, Anda hanya perlu memulai dengan membuat akun profil <a href="http://www.ruangguru.com/duta"><span class="blue-text bold">di sini</span></a>. Selanjutnya Anda akan mendapatkan Kode <i>Referral</i> yang dapat dilihat di bagian profil Duta. Kode <i>Referral</i> ini Anda berikan kepada Calon Guru dan Murid preferensi Anda. Dan pastikan bahwa Calon Guru dan Murid yang direkrut telah memasukan Kode <i>Referral</i> Anda di bagian Kode <i>Referral</i>. Karena kompensasi Duta akan diberikan apabila Guru dan Murid yang memasukkan Kode <i>Referral</i> Duta melakukan program paket belajar.
                </div>
            </li>
        </ul>
    </div>
